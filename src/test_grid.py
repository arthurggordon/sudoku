#!/usr/bin/python3
from grid import Grid

sudoku1 = [
    [0, 1, 0, 0, 0, 5, 0, 8, 3],
    [0, 0, 0, 8, 0, 0, 0, 2, 0],
    [0, 0, 0, 0, 9, 0, 0, 0, 5],
    [3, 0, 0, 0, 6, 9, 0, 0, 7],
    [0, 0, 6, 0, 0, 0, 5, 0, 0],
    [4, 0, 0, 7, 3, 0, 0, 0, 6],
    [7, 0, 0, 0, 4, 0, 0, 0, 0],
    [0, 4, 0, 0, 0, 3, 0, 0, 0],
    [8, 6, 0, 2, 0, 0, 0, 1, 0],
]

sudoku2 = [
    [0, 0, 0, 0, 0, 0, 0, 8, 3],
    [0, 0, 0, 8, 0, 0, 0, 2, 0],
    [0, 0, 0, 0, 9, 0, 0, 0, 5],
    [3, 0, 0, 0, 6, 9, 0, 0, 7],
    [0, 0, 6, 0, 0, 0, 5, 0, 0],
    [4, 0, 0, 7, 3, 0, 0, 0, 6],
    [7, 0, 0, 0, 4, 0, 0, 0, 0],
    [0, 4, 0, 0, 0, 3, 0, 0, 0],
    [8, 6, 0, 2, 0, 0, 0, 1, 0],
]


def test_one_solution():

    g = Grid(sudoku1)
    g.solve()
    assert g.count == 1
    assert g.solutions == [
        [
            [2, 1, 9, 6, 7, 5, 4, 8, 3],
            [5, 3, 7, 8, 1, 4, 6, 2, 9],
            [6, 8, 4, 3, 9, 2, 1, 7, 5],
            [3, 2, 1, 5, 6, 9, 8, 4, 7],
            [9, 7, 6, 4, 2, 8, 5, 3, 1],
            [4, 5, 8, 7, 3, 1, 2, 9, 6],
            [7, 9, 2, 1, 4, 6, 3, 5, 8],
            [1, 4, 5, 9, 8, 3, 7, 6, 2],
            [8, 6, 3, 2, 5, 7, 9, 1, 4],
        ]
    ]


def test_multiple_solutions():

    g = Grid(sudoku2)
    g.solve()
    assert g.count == 17
